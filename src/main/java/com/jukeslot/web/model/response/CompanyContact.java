package com.jukeslot.web.model.response;

public class CompanyContact {

    public String assigned_site;
    public String department;
    public String cellphone;
    public String email;
    public String name;
    public String job_title;
    public String land_line_phone;

    public String getAssigned_site() {
        return assigned_site;
    }

    public void setAssigned_site(String assigned_site) {
        this.assigned_site = assigned_site;
    }

    public String getDepartment() {
        return department;
    }

    public void setDepartment(String department) {
        this.department = department;
    }

    public String getCellphone() {
        return cellphone;
    }

    public void setCellphone(String cellphone) {
        this.cellphone = cellphone;
    }

    public String getEmail() {
        return email;
    }

    public void setEmail(String email) {
        this.email = email;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public String getJob_title() {
        return job_title;
    }

    public void setJob_title(String job_title) {
        this.job_title = job_title;
    }

    public String getLand_line_phone() {
        return land_line_phone;
    }

    public void setLand_line_phone(String land_line_phone) {
        this.land_line_phone = land_line_phone;
    }
}
